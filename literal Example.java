public class java
{
	public static void main(String[] args)
	{
		int var1 = 10;		// 10진수
		System.out.println(var1);
		
		int var2 = 010;		// 8진수
		System.out.println(var2);
		
		int var3 = 0x20;	// 16진수
		System.out.println(var3);
		
		double var4 = 0.24;
		System.out.println(var4);
		
		double var5 = 2E5;	// 2 곱하기 10의5승
		System.out.println(var5);
	}
}
